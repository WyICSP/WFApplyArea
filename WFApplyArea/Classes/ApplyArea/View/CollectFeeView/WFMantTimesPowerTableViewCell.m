#import "WFMantTimesPowerTableViewCell.h"

@implementation WFMantTimesPowerTableViewCell

static NSString *const cellId = @"WFMantTimesPowerTableViewCell";

+ (instancetype)cellWithTableView:(UITableView *)tableView {
    WFMantTimesPowerTableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:cellId];
    if (cell == nil) {
        cell = [[[NSBundle bundleForClass:[self class]] loadNibNamed:@"WFMantTimesPowerTableViewCell" owner:nil options:nil] firstObject];
    }
    return cell;
}

- (void)awakeFromNib {
    [super awakeFromNib];
    // Initialization code
    self.selectionStyle = 0;
}

- (void)setSelected:(BOOL)selected animated:(BOOL)animated {
    [super setSelected:selected animated:animated];

    // Configure the view for the selected state
}

@end
